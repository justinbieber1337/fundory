const supported = ["en", "ru"] as const;
type Lang = (typeof supported)[number];

const format = (text: string, params?: I18nParams) => {
  if (!params) return text;
  return text.replace(/\{(\w+)\}/g, (match, key) => {
    const value = params[key];
    return value === undefined ? match : String(value);
  });
};

export const useI18n = () => {
  const language = useState<Lang>("ui-language", () => "ru");

  const setLanguage = (lang: Lang) => {
    language.value = lang;
    if (typeof localStorage !== "undefined") {
      localStorage.setItem("ui_language", lang);
    }
  };

  if (process.client) {
    const saved = localStorage.getItem("ui_language");
    if (saved && supported.includes(saved as Lang)) {
      language.value = saved as Lang;
    }
  }

  const t = (key: string, params?: I18nParams) => {
    const dict = messages[language.value] || messages.ru;
    const value = dict[key as keyof typeof dict] || messages.en[key as keyof typeof messages.en] || key;
    return format(String(value), params);
  };

  return { t, language, setLanguage };
};
